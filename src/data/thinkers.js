// 学派名からCSSクラス名と色を取得するヘルパー関数
export const getSchoolTheme = (school) => {
  switch (school) {
    case '理性学派':
      return { className: 'school-reason', color: 'var(--reason-color)' };
    case '二元論学派':
      return { className: 'school-dualism', color: 'var(--dualism-color)' };
    case '内光学派':
      return { className: 'school-inner-light', color: 'var(--inner-light-color)' };
    default:
      return { className: '', color: 'var(--text-color)' };
  }
};
